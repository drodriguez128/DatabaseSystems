            <h2 class="blog-post-title">Blood Drive</h2>
            <p class="blog-post-meta">December 14, 2013 by <a href="#">Chris</a></p>

            <p>Blood drives are one of the many ways our Knight family demonstrates its giving spirit.  Come join us on the Big Red Bus!</p>

            <p>The giving spirit of our Knight family shines in many ways. The University of Central Florida is one of our region’s most prolific blood donor organizations.  Since 2010, UCF has collected 11,032 pints of blood ~33,100 lives saved!! Regular blood drives at UCF collect an average of 40 units each from students, faculty, and staff. Our World Blood Donor Day blood drives are an opportunity to join the international voice of gratitude to donors and support for recipients.</p>
            <p>Your donation makes a difference! Every donation (one pint) can save three lives, and every two seconds someone needs blood.  The only source is voluntary blood donors like you.  If all donors gave three times per year, blood shortages would be rare (the current average for regular blood donors is about two per year). Only 39% of the U.S. population is eligible to donate blood, and only 5-10% donate regularly.</p>
