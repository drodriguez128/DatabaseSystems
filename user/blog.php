            <p class="blog-post-meta">March 28, 2015 by <a href="#">Kelly</a></p>

            <h3>UCF Students Dared to Make a Difference...</h3>
            <p>Knight-Thon at the University of Central Florida began in 1996 as UCF's Dance Marathon. In their 19 year history, they have grown from a small program taking place in the university's gym raising $8,599, to a movement that is now the largest student run philanthropy on their campus. This past weekend, Knight-Thon 2015 took place on UCF's campus with more student participants than ever before in a bigger and better venue - the CFE Arena. </p>

            <p>More than 1,000 UCF students came together to celebrate their year-round fundraising efforts through dancing, playing games, enjoying time with Miracle Families, learning more about our local CMN Hospitals and standing on their feet for 20 straight hours. That's right - these UCF students stood for 20 hours For The Kids!</p>

