            <h2 class="blog-post-title">National Beer Day</h2>
            <p class="blog-post-meta">April 7, 2015 by <a href="#">Tim</a></p>

            <p>Contrary to popular belief, not every day is National Beer Day. This very important holiday is held annually on April 7, and to celebrate, we're counting some of the reasons beer is a beverage we love above all others.</p>
            <hr>
            <p>Recognized nationwide among beer enthusiasts, National Beer Day is an unofficial holiday in the United States celebrated every year on April 7, celebrating the day in 1933, the first day in 13 years, that people could legally buy, sell, and drink beer.</p>
            <blockquote>
              <p>National Beer Day is a celebration of the Cullen–Harrison Act being signed into law by President Franklin D. Roosevelt on March 22, 1933. That law went into effect on April 7 of that year, allowing people to buy, sell and drink beer again as long as it was < 3.2% alcohol-by-weight (4.05% alcohol-by-volume). People across the country responded by gathering outside breweries, some beginning the night before. On that first day, 1.5 million barrels of beer were consumed, inspiring the future holiday. Today, April 7 is recognized as National Beer Day and April 6 is known as New Beer's Eve.[1]</p>
            </blockquote>

            <h3>Beer is great for the economy.</h3>
            <p>According to the Beer Institute, more than two million Americans earn their livelihoods thanks to beer—farming grains and other ingredients, working in retail outlets that sell beer, actually brewing beer, and so on—bringing in a total of $79 billion in wages and benefits annually. Brewers, importers, and distributors also cough up $5.3 billion in federal and state excise taxes each year.</p>
           
            <h3>You can afford one after just five minutes of work.</h3>
            <p>Using median hourly wages and local beer prices around the world, analysts found out that Americans have to work the least amount of time to cover the cost of a cold brew. The data, revealed in an Economist infographic, indicated that as of 2012, the average worker on earth had to toil for 20 minutes in order to earn enough money to pay for a beer—which averaged a retail price (at the store, not bars or restaurants) of $1.55 at the time. In the U.S., where wages are much higher, employees must suffer through only five minutes of work before earning enough scratch to cover the cost of a cold one ($1.80). Everybody all together: USA! USA! USA!</p>

            <h3>Beer variety and quality have never been better.</h3>
            <p>The Brewers Association, which represents America’s craft beer movement, reported that as of 2014, there were 3,418 craft brewers in the U.S., an increase of nearly 20% over the year before. Craft beer production rose 42% last year, and for the first time ever, craft brews accounted for more than 10% of all beer sales in America.</p>

